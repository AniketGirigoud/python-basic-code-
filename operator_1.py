a = 5 
b = 2 
print("answer of", a, " + ", b, "=", a + b)
print("answer of", a, " - ", b, "=", a - b)
print("answer of", a, " / ", b, "=", a / b)
print("answer of", a, " **", b, "=", a ** b)
print("answer of", a, " % ", b, "=", a % b)
print("answer of", a, " // ", b, "=", a // b)
print(a - b)
print(a * b)
print(a / b)
print(a ** b)
print(a % b)
print(a // b) #here 2.5 answer converts to only 2 